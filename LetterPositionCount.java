public class LetterPositionCount
{
    public static void main(String args[])
    {
         String s="BHUSHAN BHAU";
         char[] a=s.toCharArray();
         int i=1;
         {
             for(char output:a)
             {
                 System.out.println(output+""+i+"");
                 i++;
                 
             }
         }
    }
}